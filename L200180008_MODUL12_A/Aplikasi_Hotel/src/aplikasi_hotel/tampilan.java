    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String tamu = "INSERT INTO tamu(tamu_nama,alamat,no_hp) VALUE (?,?,?)";
        String kamar = "INSERT INTO kamar(kamar_kode,kelas,tarif) VALUE (?,?,?)";
        String thk = "INSERT INTO tamu_has_kamar(tamu_nama, kamar_kode) VALUE (?,?)";
        String transaksi = "INSERT INTO transaksi(kode_transaksi,kamar_kode,tamu_nama) VALUE (?,?,?)";
        String tht = "INSERT INTO tamu_has_transaksi(tamu_nama, kode_transaksi) VALUE (?,?)";

        try{
            pst = conn.prepareStatement(tamu);
            pst.setString(1, nama.getText());
            pst.setString(2, alamat.getText());
            pst.setString(3, hp.getText());
            pst.execute();

            pst = conn.prepareStatement(kamar);
            pst.setString(1, kode.getText());
            String value = kelas.getSelectedItem().toString();
            pst.setString(2, value);
            pst.setString(3, tarif.getText());
            pst.execute();

            pst = conn.prepareStatement(thk);
            pst.setString(1, nama.getText());
            pst.setString(2, kode.getText());
            pst.execute();
            
            pst = conn.prepareStatement(transaksi);
            pst.setString(1, kode_transaksi.getText());
            pst.setString(2, kode.getText());
            pst.setString(3, nama.getText());
            pst.execute();
        
            pst = conn.prepareStatement(tht);
            pst.setString(1, nama.getText());
            pst.setString(2, kode_transaksi.getText());
            pst.execute();


          
            JOptionPane.showMessageDialog(null, "Berhasil disimpan");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal menyimpan data");
        }viewData();

    }//GEN-LAST:event_saveActionPerformed

