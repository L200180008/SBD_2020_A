    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
      
      try{
          String tht = "UPDATE tamu_has_transaksi set kode_transaksi=? WHERE tamu_nama=?";
          pst = conn.prepareStatement(tht);
          pst.setString(1, kode_transaksi.getText());
          pst.setString(2, nama.getText());
          pst.executeUpdate();
          
          String thk = "UPDATE tamu_has_kamar set kamar_kode=? WHERE tamu_nama=?";
          pst = conn.prepareStatement(thk);
          pst.setString(1, kode.getText());
          pst.setString(2, nama.getText());
          pst.executeUpdate();
          
          String transaksi = "UPDATE transaksi set kamar_kode=? WHERE kode_transaksi=?";
          pst = conn.prepareStatement(transaksi);
          pst.setString(1, kode_transaksi.getText());
          pst.setString(2, nama.getText());
          pst.executeUpdate();
          
          String kamar = "UPDATE kamar set kelas=?, tarif=? WHERE kamar_kode=?"; 
          pst = conn.prepareStatement(kamar);
          pst.setString(1, kode.getText());
          String v = kelas.getSelectedItem().toString();
          pst.setString(2, v);
          pst.setString(3, tarif.getText());
          pst.executeUpdate();
          
          String tamu = "UPDATE tamu set alamat=?, no_hp=? WHERE tamu_nama=?";
          pst = conn.prepareStatement(tamu);
          pst.setString(1, nama.getText());
          pst.setString(2, alamat.getText());
          pst.setString(3, hp.getText());
          pst.executeUpdate();
          
      
          JOptionPane.showMessageDialog(this, "Berhasil update");
      }catch(Exception e){
          JOptionPane.showMessageDialog(this, "Gagal update data");
      }viewData();
    }//GEN-LAST:event_updateActionPerformed

